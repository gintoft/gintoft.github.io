function clickHandler(id){

    console.log('Кликнуто по ИД: ' + id);

    // Покрасить эмоджи при клике

    markCode = id.split('_')[2]; // Код оценки

    pointsCount = 0; // обнуляем количество очков

    if (markCode == 1){

        $("#"+id).css('color', '#27AE60');
        pointsCount = 2;

    } else if (markCode == 2){

        $("#"+id).css('color', '#7FB3D5');
        pointsCount = 1;

    } else if (markCode == 3){

        $("#"+id).css('color', '#AF7AC5');
        pointsCount = 0;

    } else if (markCode == 4){

        $("#"+id).css('color', '#F5B041');
        pointsCount = -1;

    } else if (markCode == 5){

        $("#"+id).css('color', '#CD6155');
        pointsCount = -2;

    }

    // Изменить текущий рейтинг категории

    catID = parseInt(id.split('_')[1]); // ИД Категории;
    console.log('ИД Категории: ' + catID);

    categoryInfo[catID].currentRating += pointsCount; // изменение рейтинга категории

    $("#" + catID + '_rating').html(categoryInfo[catID].currentRating);

    // Обновить графики

    drawSpider();
    drawChartInteresov();
    refreshClusterRating();

}

