function dravCell(event, speaker, datetime, place, enentID, link, currentCell, tooltipText) {
    
    html  = '<h6 id="header_' + enentID + '"><a href="' + link + '" target="_blank" data-toggle="tooltip" data-placement="top" title="' + tooltipText + '">' + event + '</a></h6>';
    html += '<span id="time_' + enentID + '" class="badge badge-warning"><i class="fas fa-calendar-day"></i> ' + datetime + '</span>  ';
    html += '<span id="speaker_' + enentID + '" class="badge badge-success"><i class="fas fa-user"></i> ' + speaker + '</span>  <br>';
    html += '<span id="place_' + enentID + '"class="badge badge-light"><i class="fas fa-building"></i> ' + place + '</span>  <br>';
    
    $(currentCell).append(html);
    
}

function fillTable(){
    
    for (i = 0; i < events.length; i++){
        
        currentCell = i + 1;
        dravCell(events[i].event, events[i].speaker, events[i].datetime, events[i].place, events[i].eventID, events[i].link, '#' + currentCell, events[i].tooltipText);
        
    }
}

function changeBadge(id){
    
//    as
    
    $('#time_' + id).removeClass('badge-warning');
    $('#time_' + id).addClass('badge-light');
    
    $('#speaker_' + id).removeClass('badge-success');
    $('#speaker_' + id).addClass('badge-light');
    
    $('#place_' + id).removeClass('badge-success');
    $('#place_' + id).addClass('badge-light');
    
}